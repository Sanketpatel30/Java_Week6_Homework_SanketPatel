package HomeworkWeek6;
// Write a Java program to swap two variables

public class Project15 {

    public static void main(String[] args) {

        int temp;
        int a = 50;
        int b = 100;

        //swapping steps
        temp = a;
        a = b;
        b = temp;

        System.out.println("a = " + a + " and b = " + b );
    }
}
